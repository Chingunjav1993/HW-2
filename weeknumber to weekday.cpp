#include<iostream>
using namespace std;
int main(){
	
	char a;
	
	cout << " Please enter week number : ";
	
	cin >> a;
	
	if ( a == '1' )
	
	{
		
		cout << a << " is Monday";
		
	}
	
	else if ( a == '2' )
	
	{
		
		cout << a << " is Tuesday";
		
	}
	
	else if ( a == '3' )
	
	{
		
		cout << a << " is Wednesday";
		
	}
	
	else if ( a == '4' )
	
	{
		
		cout << a << " is Thursday";
		
	}
	
	else if ( a == '5' )
	
	{
		
		cout << a << " is Friday";
		
	}
	
	else if ( a == '6' )
	
	{
		
		cout << a << " is Saturday";
		
	}
	
	else if ( a == '7' )
	
	{
		
		cout << a << " is Sunday";
		
	}
	
	else
	
	{
		
		cout << " Choose between 1-7 please ";
	
	}
	
	
	
	
	
	
	
	
	
	
	
	return 0;
}
